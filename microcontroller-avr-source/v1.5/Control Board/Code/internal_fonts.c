unsigned int CalcMemOffset(unsigned char lang /*0 = Farsi, 1 = English*/, unsigned char CharHeight, BOOL SmallWidth, unsigned char c, unsigned char *MaxSize)
{
  //lang: 0 = Farsi, 1 = English
  unsigned int MemOffset;
  unsigned char ID, temp;
  
  switch(CharHeight)
  {
    case 8:
      if(SmallWidth)
        temp = ID_HEIGHT8_SMALL;
      else
        temp = ID_HEIGHT8;
      break;
    case 16:
      temp = ID_HEIGHT16;
      break;
    case 24:
      temp = ID_HEIGHT24;
      break;
    case 32:
      temp = ID_HEIGHT32;
      break;
  }
  ID = (lang << 7) | temp;
  
  if(c >= '0' && c <= '9')  //Is regular character (digit)?
  {
    for(temp = 0; temp < NORMAL_CHAR_COUNT; temp++)
    {
      if(InternalDigitsFont[temp].ID == ID)
      {
        MemOffset = InternalDigitsFont[temp].StartOffset + (unsigned int) (c - '0') * (unsigned int) InternalDigitsFont[temp].BytesPerChar;
        (*MaxSize) = InternalDigitsFont[temp].BytesPerChar;
        break;
      }
    }
  }
  else
  {
    //**Special characters cannot have ID_HEIGHT8_SMALL tag in this version
    ID = (ID | ID_EN | ID_SPECIAL) & (~ID_HEIGHT8_SMALL);  //All special characters are EN (in fact are same for both languages)
    
    for(temp = 0; temp < SPECIAL_CHAR_COUNT; temp++)
    {
      if((InternalSpecialFonts[temp].c == c) && (InternalSpecialFonts[temp].ID == ID))
      {
        MemOffset = InternalSpecialFonts[temp].Offset;
        (*MaxSize) = InternalSpecialFonts[temp].MaxSize;
        break;
      }
    }
  }
  
  return(MemOffset);
}

